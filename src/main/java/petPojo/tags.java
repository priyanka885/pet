package petPojo;

public class tags {
	
	
	
		
		
		int id;
		String name;
		
		public tags(int id, String name) {
			this.id = id;
			this.name = name;
		}
		
		public tags() {
			super();
		}

		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}

	

}
